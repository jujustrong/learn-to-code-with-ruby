        when "slytherin"
          puts "-"*50
          puts
          puts "Slytherin! Where Ambition Meets Cunning!"
          puts
          puts house_asciis[2]
          pause_and_clear
        when "ravenclaw"
          puts "-"*50
          puts
          puts "Ravenclaw! Where Wit Meets Wisdom! Best House Ever!"
          puts
