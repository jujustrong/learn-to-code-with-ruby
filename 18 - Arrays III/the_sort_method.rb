# sort method - sorts an array in ascending/descending order

p [2,3,1].sort
p [2,3,1].sort.reverse

p ["Hello", "Blah", "Zebra"].sort
p ["Hello", "Blah", "Zebra"].sort.reverse
p ["Hello", "zebra", "Blah", "Zebra", "artists"].sort  # Ruby considers all capital letters to come before lowercase letters

